let dice x = match x with
    | 1 -> "Un"
    | 2 -> "Deux"
    | 3 -> "Trois"
    | 4 -> "Quatre"
    | 5 -> "Cinq"
    | 6 -> "Six"
    | _ -> "Error"

let () = 
    print_endline (dice 2);
    print_endline (dice 7)
